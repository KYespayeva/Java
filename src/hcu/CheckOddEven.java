package hcu;

public class CheckOddEven {
    public static void main(String [] args){
        int number = 49;
        System.out.println("Number equal to " + number);
        if ((number%2)==0){
            System.out.println("Even");
        }
        else {
            System.out.println("Odd");
        }
        System.out.println("DONE");
    }
}
